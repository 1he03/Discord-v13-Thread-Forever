const {Client, Intents} = require("discord.js");
const intents = new Intents([ "GUILDS" ]);
const client = new Client({ intents });

//////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////

client.on("ready",()=> console.log(`${client.user.tag} is ready`));

client.on("threadUpdate",async (oThread, nThread)=>
{
	let bot = nThread.guild.members.cache.get(client.user.id); // Your bot
	if(!bot.permissions.has("SEND_MESSAGES_IN_THREADS")) return; // Check bot permissions has [ SEND_MESSAGES_IN_THREADS ]
	if(nThread.archived) nThread.setArchived(false); // Disable Archived
});

//////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////

client.login("Token Here");